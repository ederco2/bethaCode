
public class ControleSwitch {
    public ControleSwitch() {
    }

    public static void main(String[] args) {
        char letra = 67;
        switch(letra) {
            case 65:
                System.out.println("A");
                break;
            case 66:
                System.out.println("B");
                break;
            default:
                System.out.println("?");
        }

    }
}
