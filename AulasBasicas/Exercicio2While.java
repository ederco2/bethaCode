//Escreva um laço while que execute 20 vezes, imprimindo o valor da variável x
// que inicialmente está com valor 10.
public class Exercicio2While {
    public static void main(String[] args){
        int x=10;
        int y=0;
        while (y<=20){
            System.out.println("X: "+x);
            x++;
            y++;
        }
    }
}
