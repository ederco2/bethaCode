package Construcao;


class contrucao {
        private String projeto;
        private Integer qtdSala;
        private Integer qtdQuartos;
        private Integer qtdBanheiros;
        private Integer tamanhoM2;

        public String getProjeto() {
            return projeto;
        }

        public void setProjeto(String projeto) {
            this.projeto = projeto;
        }

        public Integer getQtdSala() {
            return qtdSala;
        }

        public void setQtdSala(Integer qtdSala) {
            this.qtdSala = qtdSala;
        }

        public Integer getQtdQuartos() {
            return qtdQuartos;
        }

        public void setQtdQuartos(Integer qtdQuartos) {
            this.qtdQuartos = qtdQuartos;
        }

        public Integer getQtdBanheiros() {
            return qtdBanheiros;
        }

        public void setQtdBanheiros(Integer qtdBanheiros) {
            this.qtdBanheiros = qtdBanheiros;
        }

        public Integer getTamanhoM2() {
            return tamanhoM2;
        }

        public void setTamanhoM2(Integer tamanhoM2) {
            this.tamanhoM2 = tamanhoM2;
        }
    }


